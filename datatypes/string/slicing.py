word = "Hello, World!"


#It is like an array start from 2 index but end with n-1 index
print(word[2:5])  #llo

# Slice From the Start
print(word[:5])   #Hello

# Slice To the End
print(word[7:])   #World!



# Negative Indexing
# here the concept is reversed the string start with n-1 index and end with exact index
print(word[-5:-2])  #orl


