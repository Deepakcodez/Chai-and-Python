string1 = "   hello, World     "

# to uppercase
print(string1.upper())

# to lowercase
print(string1.lower())


# Remove Whitespace
print(string1.strip())


#Replace String
print(string1.replace("World","duniya"))

#Split String
#The split() method returns a list where the text between the specified separator becomes the list items.
print(string1.split(","))
print(string1.strip().split(","))