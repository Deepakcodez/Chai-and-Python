#Number Data Type

# There are three numeric types in Python:

# int
# float
# complex

x = 1    # int
y = 2.8  # float
z = 1j   # complex
print(type(x)," ", x)
print(type(y)," ", y)
print(type(z)," ", z)



# type conversion

a = float(x)
b = int(y)
c = complex(x)

print(type(a))
print(type(b))
print(type(c))